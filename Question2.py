def print_swap_case():
    first_name = input("Enter your first name: ")
    last_name = input("Enter your last name: ")
    swapped_first_name = first_name.swapcase()
    swapped_last_name = last_name.swapcase()
    
    
    print(f"Swapped Case Name: {swapped_first_name} {swapped_last_name}")


print_swap_case()
